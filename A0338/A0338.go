func countBits(num int) []int {
	ans := make([]int, num)
	ans = append(ans, 0)
	for i := 1; i <= num; i++ {
		ans[i] = popCount(i)
	}
	return ans
}

func popCount(x int) int {
	x = x - ((x >> 1) & 0x5555555555555555)
	x = (x & 0x3333333333333333) + ((x >> 2) & 0x3333333333333333)
	x = (x + (x >> 4)) & 0x0f0f0f0f0f0f0f0f
	x = x + (x >> 8)
	x = x + (x >> 16)
	x = x + (x >> 32)
	return x & 0x7f
}
