type TreeNode struct {
	Val   int
	Left  *TreeNode
	Right *TreeNode
}

func preorderTraversal(root *TreeNode) []int {
	var preorder func(node *TreeNode)
	vals := make([]int, 0)
	preorder = func(node *TreeNode) {
		if node == nil {
			return
		}

		vals = append(vals, node.Val)
		preorder(node.Left)
		preorder(node.Right)
	}
	preorder(root)
	return vals
}
